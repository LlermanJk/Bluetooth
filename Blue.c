//Librería estándar que definie variables, macros para utilizar las funciones de entrada/salida
#include <stdio.h>
//Librería que contiene funciones C para la gestión de memoria dinámica, control de procesos. 
#include <stdlib.h>
// Librería que define diversas constantes y tipos simbólicos, y declara diversas funciones.
#include <unistd.h>
//Librería para la creación de sockets
#include <sys/socket.h>
//
#include <bluetooth/bluetooth.h>
#include <bluetooth/hci.h>
#include <bluetooth/hci_lib.h>

int main(int argc, char **argv)
{
    inquiry_info *ii = NULL;
    int max_rsp, num_rsp;
    int dev_id, sock, len, flags;
    int i;
    char addr[19] = { 0 };
    char name[248] = { 0 };
    
    dev_id = hci_get_route(NULL);
    /*Se abre un socket bluetooth con el dev_id, representa una conexión 
    al microcontrolador en el adaptador local especificado */
    sock = hci_open_dev( dev_id );
    if (dev_id < 0 || sock < 0) {
        perror("opening socket");
        exit(1);
    }

    len  = 8;
    max_rsp = 255;
    /* si las banderas se establecen en IREQ_CACHE_FLUSH, la memoria caché de
    los dispositivos detectados se vacía antes de realizar la consulta actual,
    de lo contrario, si las banderas están establecidas en 0 , los resultados
    de las consultas pueden ser devueltas inclusi si los dispositivos 
    no están en el rango*/
    flags = IREQ_CACHE_FLUSH;
    ii = (inquiry_info*)malloc(max_rsp * sizeof(inquiry_info));
    
    /*hci_inquiry realiza el descubrimiento del dispositivo bluetooth y 
    devuelve una lista de dispositivos detectadosy alguna información básica
    en la varible ii. hci_inquiry, requiere un número de recurso por lo que se
    utiliza dev_id obtenido por hci _get_route*/

    num_rsp = hci_inquiry(dev_id, len, max_rsp, NULL, &ii, flags);
    if( num_rsp < 0 ) perror("hci_inquiry");

    for (i = 0; i < num_rsp; i++) {
	/*ba2str, toma un paquete de 6 Bytes y lo transforma en una cadena   
        XX:XX:XX:XX:XX:XX*/
        ba2str(&(ii+i)->bdaddr, addr);
        memset(name, 0, sizeof(name));
 	/* Una vez encontrado los dispositivos ercans y sus direcciones, el 
   	programa determina los nombres fáciles de usar asociados con esas 
	direcciones y las presenta al usuario */

        if (hci_read_remote_name(sock, &(ii+i)->bdaddr, sizeof(name), 
            name, 0) < 0)
        strcpy(name, "[unknown]");
        printf("%s  %s\n", addr, name);
    }

    free( ii );
    close( sock );
    return 0;
}